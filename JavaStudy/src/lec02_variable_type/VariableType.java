package lec02_variable_type;

public class VariableType {
	
	// 변수의 타입
	
	// 정수 타입
	byte byteVar = 1;
	short shortVar = 2;
	int intVal = 3;
	long longVal = 4;
	char charVar = 5;
	char charVal = 'a';
	
	// 실수 타입
	float floatVar = 1.23f;
	float floatVal = 1.23F;
	double doubleVar = 1.24;
	
	// 불리언 타입 (true/false)
	boolean boolVar = true;		// 참
	boolean boolVal = false;	// 거짓
	
	String strVar = " counting star~    ";
	
	
}
